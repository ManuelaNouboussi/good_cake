  // Traitement des donn√©es de recette pour assurer la compatibilit√©
  processRecipeData(recipe) {
    if (!recipe) return null;

    // Parser les JSON si n√©cessaire
    let ingredients = recipe.ingredients;
    if (typeof ingredients === 'string') {
      try {
        ingredients = JSON.parse(ingredients);
      } catch (e) {
        console.warn('‚ö†Ô∏è Adapter: Erreur parsing ingredients:', e);
        ingredients = [];
      }
    }

    let steps = recipe.steps;
    if (typeof steps === 'string') {
      try {
        steps = JSON.parse(steps);
      } catch (e) {
        console.warn('‚ö†Ô∏è Adapter: Erreur parsing steps:', e);
        steps = [];
      }
    }

    let nutritionInfo = recipe.nutrition_info;
    if (typeof nutritionInfo === 'string') {
      try {
        nutritionInfo = JSON.parse(nutritionInfo);
      } catch (e) {
        console.warn('‚ö†Ô∏è Adapter: Erreur parsing nutrition_info:', e);
        nutritionInfo = null;
      }
    }

    // S'assurer que les arrays sont bien des arrays
    const equipment = Array.isArray(recipe.equipment) ? recipe.equipment : [];
    const allergens = Array.isArray(recipe.allergens) ? recipe.allergens : [];

    // Retourner l'objet avec tous les champs, en maintenant la compatibilit√©
    return {
      // Champs de base
      id: recipe.id,
      title: recipe.title,
      description: recipe.description,
      ingredients: ingredients,
      steps: steps,
      category_id: recipe.category_id,
      user_id: recipe.user_id,
      image_url: recipe.image_url,
      imageUrl: recipe.image_url, // Alias pour compatibilit√©
      preparation_time: recipe.preparation_time,
      prepTime: recipe.preparation_time, // Alias pour compatibilit√©
      cooking_time: recipe.cooking_time,
      cookTime: recipe.cooking_time, // Alias pour compatibilit√©
      difficulty: recipe.difficulty,
      servings: recipe.servings,
      is_featured: recipe.is_featured,
      isFeatured: recipe.is_featured, // Alias pour compatibilit√©
      created_at: recipe.created_at,
      createdAt: recipe.created_at, // Alias pour compatibilit√©
      updated_at: recipe.updated_at,
      updatedAt: recipe.updated_at, // Alias pour compatibilit√©

      // Nouveaux champs enrichis
      equipment: equipment,
      tips: recipe.tips,
      storage_instructions: recipe.storage_instructions,
      storageInstructions: recipe.storage_instructions, // Alias pour compatibilit√©
      allergens: allergens,
      nutrition_info: nutritionInfo,
      nutritionInfo: nutritionInfo, // Alias pour compatibilit√©
      video_url: recipe.video_url,
      videoUrl: recipe.video_url, // Alias pour compatibilit√©
      source: recipe.source,
      yield_info: recipe.yield_info,
      yieldInfo: recipe.yield_info, // Alias pour compatibilit√©

      // Champs calcul√©s de la vue
      category_name: recipe.category_name,
      categoryName: recipe.category_name, // Alias pour compatibilit√©
      category: recipe.category_name, // Alias pour compatibilit√©
      category_slug: recipe.category_slug,
      author_name: recipe.author_name,
      authorName: recipe.author_name, // Alias pour compatibilit√©
      author_avatar: recipe.author_avatar,
      authorAvatar: recipe.author_avatar, // Alias pour compatibilit√©
      average_gavels: recipe.average_gavels || 0,
      averageRating: recipe.average_gavels || 0, // Alias pour compatibilit√©
      total_ratings: recipe.total_ratings || 0,
      totalRatings: recipe.total_ratings || 0, // Alias pour compatibilit√©
      acquitted_count: recipe.acquitted_count || 0,
      guilty_count: recipe.guilty_count || 0,
      improvement_count: recipe.improvement_count || 0,
      formatted_date: recipe.formatted_date,
      formattedDate: recipe.formatted_date, // Alias pour compatibilit√©
      total_time: recipe.total_time || 0,
      totalTime: recipe.total_time || 0 // Alias pour compatibilit√©
    };
  }

  // Rechercher des recettes
  async searchRecipes(searchTerm, categoryName = null) {
    try {
      console.log('üì° Adapter: searchRecipes:', searchTerm, 'cat√©gorie:', categoryName);
      
      let query = this.client
        .from('recipes_complete')
        .select(`
          id,
          title,
          description,
          ingredients,
          steps,
          category_id,
          user_id,
          image_url,
          preparation_time,
          cooking_time,
          difficulty,
          servings,
          equipment,
          tips,
          storage_instructions,
          allergens,
          nutrition_info,
          video_url,
          source,
          yield_info,
          is_featured,
          created_at,
          updated_at,
          category_name,
          category_slug,
          author_name,
          author_avatar,
          average_gavels,
          total_ratings,
          formatted_date,
          total_time
        `);

      // Recherche dans le titre et la description
      if (searchTerm) {
        query = query.or(`title.ilike.%${searchTerm}%,description.ilike.%${searchTerm}%`);
      }

      // Filtrer par cat√©gorie si sp√©cifi√©
      if (categoryName && categoryName !== 'all') {
        query = query.eq('category_name', categoryName);
      }

      query = query.order('created_at', { ascending: false });

      const { data, error } = await query;

      if (error) {
        console.error('‚ùå Adapter: Erreur searchRecipes:', error);
        throw new Error(`Erreur recherche: ${error.message}`);
      }

      console.log('‚úÖ Adapter: R√©sultats recherche:', data?.length || 0);

      // Traitement des donn√©es
      const processedRecipes = (data || []).map(recipe => this.processRecipeData(recipe));

      return processedRecipes;
    } catch (error) {
      console.error('‚ùå Adapter: Erreur searchRecipes:', error);
      throw error;
    }
  }

